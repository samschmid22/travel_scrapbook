const usStateByCode = new Map(usStates.map((state) => [state.code, state]));

const usStateByNormalizedName = new Map(
  usStates.map((state) => [
    state.name
      .toLowerCase()
      .normalize("NFD")
      .replace(/[\u0300-\u036f]/g, "")
      .replace(/[^a-z0-9]+/g, " ")
      .trim(),
    state,
  ]),
);

export function getUSStateCodeFromRegion(region?: string) {
  if (!region?.trim()) {
    return undefined;
  }

  const cleaned = region.trim();
  const upper = cleaned.toUpperCase();

  if (upper.length === 2 && usStateByCode.has(upper)) {
    return upper;
  }

  const normalized = cleaned
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();

  return usStateByNormalizedName.get(normalized)?.code;
}

export function getUSStateNameFromCode(code: string) {
  return usStateByCode.get(code.toUpperCase())?.name;
}
